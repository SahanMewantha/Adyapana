    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        String newAddress = jTextField4.getText();
        int selectedrow = jTable1.getSelectedRow();

        if (selectedrow == -1) {
            JOptionPane.showMessageDialog(this, "Please Select a Teacher", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (newAddress.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter New Address", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {

            String teacher_id = jTable1.getValueAt(selectedrow, 0).toString();

            int x = JOptionPane.showConfirmDialog(this, "Do you want to update teacher address?", "Warning", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);

            if (x == JOptionPane.YES_OPTION) {
                MySQL.iud("UPDATE  `teacher` SET `address`='" + newAddress + "' WHERE `tno`='" + teacher_id + "'");
                JOptionPane.showMessageDialog(this, "Teacher Address Updated", "Success", JOptionPane.INFORMATION_MESSAGE);
            }

        }
        resetFields();
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int selectedrow = jTable1.getSelectedRow();

        if (selectedrow == -1) {
            JOptionPane.showMessageDialog(this, "Please Select a Teacher", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {

            String teacher_id = jTable1.getValueAt(selectedrow, 0).toString();

            int x = JOptionPane.showConfirmDialog(this, "Do you want to delete Teacher detail?", "Warning", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);

            if (x == JOptionPane.YES_OPTION) {
                try {

                    ResultSet rs2 = MySQL.search("SELECT * FROM `teacher_has_subject` WHERE `teacher_tno`='" + teacher_id + "'");
                    if (rs2.next()) {
                        MySQL.iud("DELETE FROM `teacher_has_subject` WHERE `teacher_tno`='" + teacher_id + "'");
                    }

                } catch (Exception e) {
                    e.printStackTrace();
                }

                MySQL.iud("DELETE FROM `teacher` WHERE `tno`='" + teacher_id + "'");
                JOptionPane.showMessageDialog(this, "Delete Successfully", "Success", JOptionPane.INFORMATION_MESSAGE);
            }
        }
        loadteacher();
    }//GEN-LAST:event_jButton2ActionPerformed

