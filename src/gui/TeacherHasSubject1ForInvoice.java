    public void loadSubject() {

        try {

            ResultSet rs = MySQL.search("SELECT * FROM `subject` ORDER BY `description` ASC");

            Vector v = new Vector();
            v.add("Select");

            while (rs.next()) {
                v.add(rs.getString("description"));
            }
            jComboBox2.setModel(new DefaultComboBoxModel(v));

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

