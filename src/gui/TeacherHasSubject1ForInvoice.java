    public void loadTable() {

        try {

            ResultSet rs = MySQL.search("SELECT * FROM `teacher_has_subject` INNER JOIN `teacher` ON `teacher_has_subject`.`teacher_tno`=`teacher`.`tno` INNER JOIN `subject` ON `teacher_has_subject`.`subject_subno`=`subject`.`subno`");
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);

            while (rs.next()) {
                Vector v = new Vector();
                v.add(rs.getString("id"));
                v.add(rs.getString("teacher.name"));
                v.add(rs.getString("subject.description"));
                v.add(rs.getString("subject.price"));
                dtm.addRow(v);
            }

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

