    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if (jComboBox1.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(this, "Plese Select Teacher Name", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (jComboBox2.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(this, "Plese Select Subject Name", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {

            String teacher = jComboBox1.getSelectedItem().toString();
            String subject = jComboBox2.getSelectedItem().toString();

            try {

                ResultSet rs = MySQL.search("SELECT * FROM `teacher_has_subject` INNER JOIN `teacher` ON `teacher_has_subject`.`teacher_tno`=`teacher`.`tno` INNER JOIN `subject` ON `teacher_has_subject`.`subject_subno`=`subject`.`subno` WHERE `teacher`.`name` = '" + teacher + "' AND `subject`.`description`='" + subject + "'");

                if (rs.next()) {
                    JOptionPane.showMessageDialog(this, "Teacher Has Subject already exists", "Warning", JOptionPane.WARNING_MESSAGE);
                } else {

                    ResultSet rs1 = MySQL.search("SELECT `tno` FROM `teacher` WHERE `name`='" + teacher + "'");
                    rs1.next();

                    ResultSet rs2 = MySQL.search("SELECT `subno` FROM `subject` WHERE `description`='" + subject + "'");
                    rs2.next();

                    String teacher_tno = rs1.getString("tno");
                    String subject_subno = rs2.getString("subno");

                    MySQL.iud("INSERT INTO `teacher_has_subject`(`teacher_tno`,`subject_subno`) VALUES ('" + teacher_tno + "','" + subject_subno + "') ");
                    loadTable();

                    jComboBox1.setSelectedIndex(0);
                    jComboBox2.setSelectedIndex(0);

                    JOptionPane.showMessageDialog(this, "New Teacher Has Subject Added", "Success", JOptionPane.INFORMATION_MESSAGE);

                }

            } catch (Exception e) {
                e.printStackTrace();
            }

        }

    }//GEN-LAST:event_jButton1ActionPerformed

