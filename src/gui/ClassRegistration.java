    public void loadClassRegistation() {

        try {
            ResultSet rs = MySQL.search("SELECT * FROM `class` INNER JOIN `teacher_has_subject` ON `class`.`teacher_has_subject_id`=`teacher_has_subject`.`id` INNER JOIN `subject` ON `teacher_has_subject`.`subject_subno`=`subject`.`subno` INNER JOIN `teacher` ON `teacher_has_subject`.`teacher_tno`=`teacher`.`tno` ");

            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);

            while (rs.next()) {
                Vector v = new Vector();
                v.add(rs.getString("subject.description"));
                v.add(rs.getString("teacher.name"));
                v.add(rs.getString("class.date"));
                v.add(rs.getString("class.time"));

                dtm.addRow(v);
            }

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

