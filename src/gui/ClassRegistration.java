    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        if (evt.getClickCount() == 2) {
            int r = jTable1.getSelectedRow();

            String subject = jTable1.getValueAt(r, 0).toString();
            String teacher = jTable1.getValueAt(r, 1).toString();
            String date = jTable1.getValueAt(r, 2).toString();
            String time = jTable1.getValueAt(r, 3).toString();

            sa.jLabel5.setText(subject);
            sa.jLabel7.setText(teacher);
            sa.jLabel9.setText(date);
            sa.jLabel10.setText(time);
            this.dispose();

        }
    }//GEN-LAST:event_jTable1MouseClicked

