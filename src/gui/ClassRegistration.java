    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        String subject = jLabel3.getText();
        String teacher = jLabel4.getText();
        Date d = jDateChooser1.getDate();
        String time1 = jLabel7.getText();
        String time2 = jLabel8.getText();

        if (subject.equals("None")) {
            JOptionPane.showMessageDialog(this, "Please Select Subject", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (d == null) {
            JOptionPane.showMessageDialog(this, "Invalid Date", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (time1.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Invalid Time", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (time2.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Invalid Time", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {

            String df = sdf.format(d);

            try {

                ResultSet rs1 = MySQL.search("SELECT * FROM `class` WHERE `time` = '" + time1 + " - " + time2 + "' AND `date`='" + df + "' ");

                if (rs1.next()) {
                    JOptionPane.showMessageDialog(this, "There is a class at that time!", "Warning", JOptionPane.WARNING_MESSAGE);

                } else {

                    ResultSet rs2 = MySQL.search("SELECT `id` FROM `teacher_has_subject` WHERE `teacher_tno` IN  (SELECT `tno` FROM `teacher` WHERE `name` = '" + teacher + "')  AND  `subject_subno` IN (SELECT `subno` FROM `subject` WHERE `description` = '" + subject + "') ");
                    rs2.next();

                    String teacher_has_subject_id = rs2.getString("id");

                    MySQL.iud("INSERT INTO `class`(`teacher_has_subject_id`,`date`,`time`)  VALUES('" + teacher_has_subject_id + "','" + sdf.format(d) + "','" + time1 + " - " + time2 + "')");
                    loadClassRegistation();

                    JOptionPane.showMessageDialog(this, "Details were successfully added!", "Success", JOptionPane.INFORMATION_MESSAGE);
                    reset();
                }

            } catch (Exception e) {
                e.printStackTrace();
            }

        }

    }//GEN-LAST:event_jButton3ActionPerformed

