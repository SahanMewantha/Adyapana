    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        String student = jLabel2.getText();
        String sid = jLabel14.getText();
        String teacher = jLabel11.getText();
        String thsid = jLabel16.getText();
        String subject = jLabel10.getText();
        String price = jLabel12.getText();

        if (student.equals("None")) {
            JOptionPane.showMessageDialog(this, "Please Select Student", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (subject.equals("None")) {
            JOptionPane.showMessageDialog(this, "Please Select Subject", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {

            try {

                ResultSet rs = MySQL.search("SELECT * FROM `student_has_subject` WHERE `student_id`='" + sid + "'");

                if (rs.next()) {
                    JOptionPane.showMessageDialog(this, "Student already exists", "Warning", JOptionPane.WARNING_MESSAGE);
                } else {

                    MySQL.iud("INSERT INTO `student_has_subject` (`student_id`,`teacher_has_subject_id`) VALUES ('" + sid + "','" + thsid + "')");

                    jLabel14.setText("None");
                    jLabel2.setText("None");
                    jLabel4.setText("None");
                    jLabel6.setText("None");
                    jLabel10.setText("None");
                    jLabel11.setText("None");
                    jLabel12.setText("None");
                    loadStudentHasSubject();

                    JOptionPane.showMessageDialog(this, "Student Has Subject Added", "Success", JOptionPane.INFORMATION_MESSAGE);

                }

            } catch (Exception e) {
                e.printStackTrace();
            }

        }

    }//GEN-LAST:event_jButton3ActionPerformed

